
def isValidSudoku(board):
    for row in range(len(board[0])):
        col_arr = []
        row_arr = []

        for col in range(len(board[1])):
            col_val = board[col][row]
            row_val = board[row][col]

            if row_val != '.':
                row_arr.append(row_val)

            if col_val != '.':
                col_arr.append(col_val)
                # print('row = {}, row_val = {}'.format(row, row_val))

            # print('row = {}, col = {}, col_val = {}'.format(row, col, col_val))
        # print('row_arr = {}, col_arr = {}'.format(row_arr, col_arr))

        if len(row_arr) != len(set(row_arr)):
            return False
        elif len(col_arr) != len(set(col_arr)):
            return False

    return True


